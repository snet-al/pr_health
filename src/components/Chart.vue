<script>
  import { Line, mixins } from 'vue-chartjs'

  export default {
    mixins: [mixins.reactiveProp],
    data: () => ({
      options: {
        responsive: true,
        maintainAspectRatio: false
      }
    }),
    extends: Line,
    props: {
      chartdata: {
        type: Object,
        default: null
      },
      options: {
        type: Object,
        default: null
      }
    },
    mounted () {
      this.renderChart(this.chartdata, this.options)
    }
  }

</script>

